		public string Type { get; } = type;

		public string Method { get; } = method;

		public string Field { get; } = field;

		public string Construction { get; } = construction;

		public bool Disposable { get; } = disposable;

		public bool RuntimeCheck { get; } = runtimeCheck;

		public string? Comment { get; } = comment;
	}

	/// <summary>
	///     The inputs to <see cref="EmitFreshResolver" />: the method <see cref="Modifiers" /> and return
	///     <see cref="Type" />, the resolver <see cref="Method" /> name, its parameter <see cref="Signature" />
	///     (empty for a transient, the runtime arguments for a parameterized service), the
	///     <see cref="Construction" /> expression, whether the instance <see cref="Disposable">needs
	///     disposal</see> and whether disposal tracking is gated by a <see cref="RuntimeCheck">runtime
	///     <c>is IDisposable</c> check</see> on the realized instance (a factory output) rather than the static
	///     flag.
	/// </summary>
	private readonly struct FreshResolver(string modifiers, string type, string method, string signature, string construction, bool disposable, bool runtimeCheck)
	{
		public string Modifiers { get; } = modifiers;

		public string Type { get; } = type;

		public string Method { get; } = method;

		public string Signature { get; } = signature;

		public string Construction { get; } = construction;

		public bool Disposable { get; } = disposable;

		public bool RuntimeCheck { get; } = runtimeCheck;
	}

	/// <summary>
	///     A single dispatch case: the service <see cref="Type" /> requested and the <see cref="Value" />
	///     assigned to <c>instance</c> (the resolver call). Every entry is dispatchable - it always carries a
	///     real resolver. When <see cref="Guidance" /> is set the entry is additionally <em>root-withheld</em>
	///     under strict lifetime safety: it resolves normally from a child scope (where its lifetime is bounded
	///     by the scope), but the <c>__rootWithheld</c> mask makes <c>TryResolve</c> return
	///     <see langword="false" /> for it on the Root and <c>Resolve</c> throw the <see cref="Guidance" />
	///     message (placed in the <c>__withheld</c> table) - so the root-accumulation leak stays impossible
	///     while the safe scope-bound resolution is allowed.
	/// </summary>
	private readonly struct DispatchEntry(string type, string value, string? guidance = null)
	{
		public string Type { get; } = type;

		public string Value { get; } = value;

		public string? Guidance { get; } = guidance;

		public bool RootWithheld => Guidance is not null;
	}

	/// <summary>
	///     Assigns each instance a readable resolver method name (<c>ResolveGreeter</c>) and cache
	///     field name (<c>_greeter</c>) derived from the implementation's simple type name, appending a
	///     numeric suffix only when two implementations would otherwise collide (case-insensitively).
	/// </summary>
	private sealed class Names
	{
		private readonly string[] _fields;
		private readonly string[] _resolvers;

		private Names(string[] resolvers, string[] fields)
		{
			_resolvers = resolvers;
			_fields = fields;
		}

		public string Resolver(int index) => _resolvers[index];

		public string Field(int index) => _fields[index];

		// The async members are named off the synchronous resolver/field so they stay collision-safe
		// together: ResolveFoo -> ResolveFooAsync / CreateFooAsync, _foo -> _fooAsyncTask.
		public string AsyncResolver(int index) => _resolvers[index] + "Async";

		public string AsyncCreator(int index) => "Create" + _resolvers[index].Substring("Resolve".Length) + "Async";

		public string AsyncField(int index) => _fields[index] + "AsyncTask";

		public static Names Build(InstanceModel[] instances)
		{
			string[] resolvers = new string[instances.Length];
			string[] fields = new string[instances.Length];
			HashSet<string> used = new(StringComparer.OrdinalIgnoreCase);

			for (int i = 0; i < instances.Length; i++)
			{
				string baseName = Sanitize(instances[i].Name);
				string name = baseName;
				int suffix = 2;
				while (!used.Add(name))
				{
					name = baseName + suffix;
					suffix++;
				}

				resolvers[i] = "Resolve" + name;
				fields[i] = "_" + char.ToLowerInvariant(name[0]) + name.Substring(1);
			}

			return new Names(resolvers, fields);
		}

		private static string Sanitize(string name)
		{
			if (string.IsNullOrEmpty(name))
			{
				return "Service";
			}

			StringBuilder builder = new(name.Length);
			foreach (char c in name.Where(c => char.IsLetterOrDigit(c) || c == '_'))
			{
				builder.Append(c);
			}

			if (builder.Length == 0 || (!char.IsLetter(builder[0]) && builder[0] != '_'))
			{
				builder.Insert(0, '_');
			}

			return builder.ToString();
		}
	}
